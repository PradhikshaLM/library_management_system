    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
       try{
           if(txtMid.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "Member id required");
               return;
           }
       
       int memberId=Integer.parseInt(txtMid.getText().trim());
       
         if(txtBid.getText().isEmpty()){
             JOptionPane.showMessageDialog(this, "Book id required");
             return;
         }
       int bookId=Integer.parseInt(txtBid.getText());
       
       java.util.Date cd=new java.util.Date();
       SimpleDateFormat df=new SimpleDateFormat("yyyy-MM-dd");
      
      
      
      
       boolean success=b.returnBook(memberId, bookId);
       if(success){
           JOptionPane.showMessageDialog(null, "Book returned successfully");
           txtMid.setText("");
           txtBid.setText("");
          
       }else{
           JOptionPane.showMessageDialog(this, "Failed to return the book");
       }
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(this, "Enter valid details");
       }
    }//GEN-LAST:event_btnReturnActionPerformed

