    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String title=txtTitle.getText().trim();
        String authorName=txtAname.getText().trim();
        ArrayList<Book>books=b1.searchFromDb(title, authorName);
        if(books.isEmpty()){
            JOptionPane.showMessageDialog(null, "No books found with the provided details");
        }else{
            DefaultTableModel t1=(DefaultTableModel)jTable1.getModel();
            t1.setRowCount(0);
            
            for(Book book:books){
                t1.addRow(new Object[]{
                    book.getId(),
                    book.getTitle(),
                    book.getPublishedYear(),
                    book.getAuthor().getName(),
                    book.getAuthor().getEmail()

                });
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

